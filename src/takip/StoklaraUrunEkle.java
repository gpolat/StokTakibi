    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        uAd = jTextField1.getText();
        String uFiyati = jTextField2.getText();
        try {
            uFiyat = Double.parseDouble(uFiyati);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lütfen geçerli bir rakam giriniz.");
            return;
        }
        
        if(db.urunEkle(uAd, 0, uFiyat))
        {
            JOptionPane.showMessageDialog(this, uAd+" adlı ürün başarıyla eklendi");
            uAd = "";
            uFiyat = 0;
            this.dispose();
            return;
        }
        else
            JOptionPane.showMessageDialog(this, uAd+" adlı ürün zaten var");
    }//GEN-LAST:event_jButton1ActionPerformed


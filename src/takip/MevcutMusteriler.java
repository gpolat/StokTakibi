    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String uName = ""+jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        int row = jTable2.getSelectedRow();
        String mName = ""+jTable2.getValueAt(row, 0);
        double sMiktari = Double.parseDouble(jTextField1.getText());
                
        if(db.satisYap(mName, uName, sMiktari))
        {
            JOptionPane.showMessageDialog(this, "Satış başarıyla gerçekleştirildi");
        }
        else
            JOptionPane.showMessageDialog(this, "Elimizde "+sMiktari+" kilo "+uName+" yok.");
    }//GEN-LAST:event_jButton3ActionPerformed


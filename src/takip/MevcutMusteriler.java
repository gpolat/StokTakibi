    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int cevap=0;
        if(jTable2.getSelectedRow() == -1)
        {
            JOptionPane.showMessageDialog(rootPane, "ÖNCE BİR MÜŞTERİ SEÇMELİSİNİZ");
            return;
        }
        else
            cevap = JOptionPane.showConfirmDialog(this, "Gerçekten Silinsin mi", "DİKKAT", JOptionPane.YES_NO_OPTION);
        
        if( cevap == JOptionPane.YES_NO_OPTION)   
        {
            int row = jTable2.getSelectedRow();
            String mName = ""+jTable2.getValueAt(row, 0);
            db.musteriSil(mName);
            jTable2.setModel(db.getMusteriler(this));
        }
        else
            return;
    }//GEN-LAST:event_jButton2ActionPerformed

